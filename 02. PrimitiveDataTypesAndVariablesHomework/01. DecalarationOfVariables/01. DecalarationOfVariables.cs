using System;

class DecalarationOfVariables
{
    static void Main()
    {
        ushort uShortVariable = 52130;
        sbyte sByteVariable = -115;
        int integerVariable = 4825932;
        byte byteVariable = 97;
        short shortVariable = -10000;

        Console.WriteLine(uShortVariable + ", " + sByteVariable + ", " + integerVariable +
            ", " + byteVariable + ", " + shortVariable + ".");
    }
}